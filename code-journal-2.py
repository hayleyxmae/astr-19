#this will add two floats
x = 2.0
y = 3.0
print("x+y = ", x+y)

#this will report the data type for x+y
print("type(x+y): ",type(x+y))

#this will subtract two integers
a = 3
b = 2
print("a-b = ",a-b)

#this will report the data type for a-b
print("type(a-b): ",type(a-b))

#this will multiply an integer and a float
c = 5
d = 4.0
print("c*d = ",c*d)

#this will report the data type for the product
print("type(c*d): ",type(c*d))
